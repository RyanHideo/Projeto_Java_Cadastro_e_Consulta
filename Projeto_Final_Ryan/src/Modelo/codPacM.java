public class codPacM {
    
    private String codigo;
    private String cpf;

    public codPacM(String codigo, String cpf) {
        this.codigo = codigo;
        this.cpf = cpf;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
    
}